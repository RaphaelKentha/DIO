#tipos de operadores em python
#operadores aritmeticos:
        # + adição
        # - subtração
        # * multiplicação
        # / divisão
        # % modulo
        # ** exponenciação
        # // divisão inteira
#operadores de atribuição:
        # = atribuição
        # += adição
        # -= subtração
        # *= multiplicação
        # /= divisão
        # %= modulo
        # **= exponenciação
        # //= divisão inteira
#operadores de bit:
        # & and
        # | or
        # ^ xor
        # ~ not
        # << deslocamento a esquerda
        # >> deslocamento a direita
#operadores de comparação:
        # == igual
        # != diferente
        # > maior
        # < menor
        # >= maior ou igual
        # <= menor ou igual
#operadores de identidade:
        # is
        # is not
#operadores de associação:
        # in
        # not in

#usando o print para mostrar o resultado dos tipos de operadores:
print(10 + 10) #adição
print(10 - 10) #subtração
print(10 * 10) #multiplicação
print(10 / 10) #divisão
print(10 % 10) #modulo
print(10 ** 10) #exponenciação
print(10 // 10) #divisão inteira

#usando o print para mostrar o resultado dos tipos de operadores de atribuição:
x = 10
x += 10 #x = x + 10
print(x)
x -= 10 #x = x - 10
print(x)
x *= 10 #x = x * 10
print(x)
x /= 10 #x = x / 10
print(x)
x %= 10 #x = x % 10
print(x)
x **= 10 #x = x ** 10
print(x)
x //= 10 #x = x // 10
print(x)

#usando o print para mostrar o resultado dos tipos de operadores de bit:
print(10 & 10) #and
print(10 | 10) #or
print(10 ^ 10) #xor
print(~10) #not
print(10 << 10) #deslocamento a esquerda
print(10 >> 10) #deslocamento a direita

#usando o print para mostrar o resultado dos tipos de operadores de comparação:
print(10 == 10) #igual
print(10 != 10) #diferente
print(10 > 10) #maior
print(10 < 10) #menor
print(10 >= 10) #maior ou igual
print(10 <= 10) #menor ou igual

#usando o print para mostrar o resultado dos tipos de operadores de identidade:
x = 10
y = 10
print(x is y)
print(x is not y)

#usando o print para mostrar o resultado dos tipos de operadores de associação:
x = [1, 2, 3, 4, 5]
print(1 in x)
print(1 not in x)

